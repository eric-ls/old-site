var adjustCoverHeight = function () {
  var titleHeight = document.querySelector(".title-container").offsetHeight;
  var cover = document.getElementById("particles-js");

  cover.style.height = titleHeight + 80 + "px";
};

window.onload = function () {
  if (window.innerWidth < 970) {
    adjustCoverHeight();
  }
};

window.onresize = function () {
  if (window.innerWidth < 970) {
    adjustCoverHeight();
  } else {
    document.getElementById("particles-js").style.height = "470px";
  }
};

/**
* Function that tracks a click on an outbound link in Google Analytics.
* This function takes a valid URL string as an argument, and uses that URL string
* as the event label. Setting the transport method to 'beacon' lets the hit be sent
* using 'navigator.sendBeacon' in browser that support it.
*/
var trackOutboundLink = function(url) {
   ga('send', 'event', 'outbound', 'click', url, {
     'transport': 'beacon',
     'hitCallback': function(){document.location = url;}
   });
}

